package Geometry;

class Point {
    private int x, y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int get_X_Values() {
        int x_copy = this.x;
        return x_copy;
    }

    public int get_Y_Values() {
        int y_copy = this.y;
        return y_copy;
    }

}


